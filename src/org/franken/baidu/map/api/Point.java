package org.franken.baidu.map.api;

/**
 * poi坐标{x,y}
 * @author frankenliu
 *
 */
public class Point {

	// 周围位置的X坐标
	private double x;
	// 周围位置的Y坐标
	private double y;

	public double getX() {
		return x;
	}

	public void setX(double x) {
		this.x = x;
	}

	public double getY() {
		return y;
	}

	public void setY(double y) {
		this.y = y;
	}
	
}
